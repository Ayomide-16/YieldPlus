const LocationSelector = ({ location, onLocationChange }: LocationSelectorProps) => {
  const { t } = useTranslation();
  const countries = Object.keys(worldLocations).sort();
  const states = location.country ? Object.keys(worldLocations[location.country] || {}) : [];
  const localGovernments = location.country && location.state 
    ? worldLocations[location.country]?.[location.state] || [] 
    : [];

  const handleCountryChange = (country: string) => {
    onLocationChange({ country, state: "", localGovernment: "" });
  };

  const handleStateChange = (state: string) => {
    onLocationChange({ ...location, state, localGovernment: "" });
  };

  const handleLocalGovernmentChange = (localGovernment: string) => {
    onLocationChange({ ...location, localGovernment });
  };

  return (
    <div className="space-y-4">
      <div className="space-y-2">
        <Label htmlFor="country">{t("common.country")}</Label>
        <Select value={location.country} onValueChange={handleCountryChange}>
          <SelectTrigger id="country">
            <SelectValue placeholder={t("common.select")} />
          </SelectTrigger>
          <SelectContent className="max-h-[300px]">
            {countries.map((country) => (
              <SelectItem key={country} value={country}>
                {country}
              </SelectItem>
            ))}
          </SelectContent>
        </Select>
      </div>

      {location.country && states.length > 0 && (
        <div className="space-y-2">
          <Label htmlFor="state">{t("common.state")}</Label>
          <Select value={location.state} onValueChange={handleStateChange}>
            <SelectTrigger id="state">
              <SelectValue placeholder={t("common.select")} />
            </SelectTrigger>
            <SelectContent className="max-h-[300px]">
              {states.map((state) => (
                <SelectItem key={state} value={state}>
                  {state}
                </SelectItem>
              ))}
            </SelectContent>
          </Select>
        </div>
      )}

      {location.state && localGovernments.length > 0 && (
        <div className="space-y-2">
          <Label htmlFor="localGov">{t("common.city")}</Label>
          <Select value={location.localGovernment} onValueChange={handleLocalGovernmentChange}>
            <SelectTrigger id="localGov">
              <SelectValue placeholder={t("common.select")} />
            </SelectTrigger>
            <SelectContent className="max-h-[300px]">
              {localGovernments.map((lg) => (
                <SelectItem key={lg} value={lg}>
                  {lg}
                </SelectItem>
              ))}
            </SelectContent>
          </Select>
        </div>
      )}
    </div>
  );
};

export default LocationSelector;
